
void main() {
  // Some comparison operations
  // Syntax: [value1] [comparator] [value2];
  // List: ==, !=, >, <, >=, <=
  int x = 10;
  int y = 20;
  print("x == y: ${x == y}"); // false
  print("x != y: ${x != y}"); // true
  print("x > y: ${x > y}"); // false
  print("x < y: ${x < y}"); // true
  print("x >= y: ${x >= y}"); // false
  print("x <= y: ${x <= y}"); // true
}